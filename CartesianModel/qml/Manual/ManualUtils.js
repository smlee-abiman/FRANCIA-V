function axisDataExpress(model, index){
    switch(index) {
    case 0:
        return model.axis1
    case 1:
        return model.axis2
    case 2:
        return model.axis3
    case 3:
        return model.axis4
    case 4:
        return model.axis5
    case 5:
        return model.axis6
    case 6:
        return model.axis7
    case 7:
        return model.axis8       
    }
}
